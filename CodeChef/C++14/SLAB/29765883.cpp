#include<bits/stdc++.h>
#define ll long long int
#define ld long double
using namespace std;
int main()
{
	ll n, t, i;
	cin >> t;
	while(t--){
		cin >> n;
		if(n <= 250000)
			cout << n << '\n';
		else if(n > 250000 && n <=500000){
			i = 0.05*(n - 250000);
			cout << n-i << '\n';
		}
		else if(n > 500000 && n <= 750000){
			i = 0.05*(500000 - 250000);
			ll m = 0.10*(n-500000);
			cout << n-(m+i) << '\n';
		}
		else if(n > 750000 && n <= 1000000){
			i = 0.05*(500000 - 250000);
			ll m = 0.10*(750000-500000);
			ll z = 0.15 *(n-750000); 
			cout << n-(m + i + z) << '\n';
		}
		else if(n > 1000000 && n <= 1250000){
			i = 0.05*(500000 - 250000);
			ll m = 0.10*(750000-500000);
			ll z = 0.15 *(1000000-750000);
			ll y = 0.20 *(n - 1000000);
			cout << n-(m + i + z + y)<< '\n';
		}
		else if(n > 1250000 && n <= 1500000){
			i = 0.05*(500000 - 250000);
			ll m = 0.10*(750000-500000);
			ll z = 0.15 *(1000000-750000);
			ll y = 0.20 *(1250000 - 1000000);
			ll l = 0.25 *(n - 1250000);
			cout << n-(m + i + z + y + l) << '\n';
		}
		else{
			i = 0.05*(500000 - 250000);
			ll m = 0.10*(750000-500000);
			ll z = 0.15 *(1000000-750000);
			ll y = 0.20 *(1250000 - 1000000);
			ll l = 0.25 *(1500000 - 1250000);
			ll e = 0.30 *(n - 1500000);
			cout << n-(m + i + z + y + l + e) << '\n';
		}
    }
	return 0;
}
	
